    public void  HienThiDSSP(){
        Database qlsp = new Database();
        Vector<SanPham> dsSP=qlsp.DSSanPham();
        if(!dsSP.isEmpty()){
            DefaultTableModel dtm = (DefaultTableModel)tbsp.getModel();
            dtm.setRowCount(0);
            for(SanPham sp : dsSP)
            {
             dtm.addRow(new Object[]{sp.getMasp(),sp.getTensp(),sp.getHinhanhsp(),sp.getGioithieusp(),sp.getGiasp()});
            }
        }
    }
