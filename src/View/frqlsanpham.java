    private void btxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btxoaActionPerformed
        // TODO add your handling code here:
        int i =this.tbsp.getSelectedRow();
        if(i<0){
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn dòng cần xóa");
            return;
        }
        DefaultTableModel model = (DefaultTableModel)this.tbsp.getModel();
        int masp=(int) model.getValueAt(i, 0);
        Database cn = new Database();
        int kq=cn.Xoasp(masp);
        if(kq>0){
            JOptionPane.showMessageDialog(this, "Xóa thành công");
            HienThiDSSP();
        }
        else
            JOptionPane.showMessageDialog(this, "Lỗi xóa san pham");
    }//GEN-LAST:event_btxoaActionPerformed

    private void btsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsuaActionPerformed
        // TODO add your handling code here:
        int i =this.tbsp.getSelectedRow();
        if(i<0){
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn dòng cần sửa");
            return;
        }
        else{
            SanPham sp = new SanPham();
            sp.masp=(int)tbsp.getModel().getValueAt(i,0);
            sp.tensp=(String)tbsp.getModel().getValueAt(i,1);
            sp.hinhanhsp=(String)tbsp.getModel().getValueAt(i,2);
            sp.gioithieusp=(String)tbsp.getModel().getValueAt(i,3);
            sp.giasp=(int)tbsp.getModel().getValueAt(i,4);
            frsuasp suasp = new frsuasp();
            suasp.sanpham=sp;
            suasp.qlsp=this;
            suasp.setVisible(true);
        }
        
    }//GEN-LAST:event_btsuaActionPerformed

